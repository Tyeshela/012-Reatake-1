// Create a variable score and assign a number to it
const score = 85;

// Use nested ternary operators to assign a grade to a variable grade based on the value of score
const grade =
  score >= 90 ? 'A' :
  score >= 80 ? 'B' :
  score >= 70 ? 'C' :
  score >= 60 ? 'D' : 'F';

// Print the grade variable to the console
console.log(grade); // Output will depend on the value of score
